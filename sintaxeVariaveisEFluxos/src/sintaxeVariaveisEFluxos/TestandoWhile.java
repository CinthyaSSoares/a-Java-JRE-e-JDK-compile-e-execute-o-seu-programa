package sintaxeVariaveisEFluxos;

public class TestandoWhile {
	public static void main(String[] args) {
		int contador = 0;
		while(contador <= 10) {
			System.out.println(contador);
			// Formas de somar o contador +1:
			// contador = contador +1;
			//contador +=1;	
			contador++;
		}
		System.out.println(contador);
	}
}
